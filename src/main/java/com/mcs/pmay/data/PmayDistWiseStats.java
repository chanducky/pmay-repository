package com.mcs.pmay.data;

public class PmayDistWiseStats {
	private String distName;
	private int slum;
	private int nonSlum;
	private String pkg;
	private String blc;
	
	public String getDistName() {
		return distName;
	}
	public void setDistName(String distName) {
		this.distName = distName;
	}
	public int getSlum() {
		return slum;
	}
	public void setSlum(int slum) {
		this.slum = slum;
	}
	public int getNonSlum() {
		return nonSlum;
	}
	public void setNonSlum(int nonSlum) {
		this.nonSlum = nonSlum;
	}
	public String getPkg() {
		return pkg;
	}
	public void setPkg(String pkg) {
		this.pkg = pkg;
	}
	public String getBlc() {
		return blc;
	}
	public void setBlc(String blc) {
		this.blc = blc;
	}
	
}
