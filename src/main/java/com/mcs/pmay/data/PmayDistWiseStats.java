package com.mcs.pmay.data;

public class PmayDistWiseStats {
	private String distName;
	private int slum;
	private int nonSlum;
	
	private String blc;
	
	private String slumType;
	private int pkg1;
	private int pkg4;
	
	public String getDistName() {
		return distName;
	}
	public void setDistName(String distName) {
		this.distName = distName;
	}
	public int getSlum() {
		return slum;
	}
	public void setSlum(int slum) {
		this.slum = slum;
	}
	public int getNonSlum() {
		return nonSlum;
	}
	public void setNonSlum(int nonSlum) {
		this.nonSlum = nonSlum;
	}
	public String getBlc() {
		return blc;
	}
	public void setBlc(String blc) {
		this.blc = blc;
	}
	public String getSlumType() {
		return slumType;
	}
	public void setSlumType(String slumType) {
		this.slumType = slumType;
	}
	public int getPkg1() {
		return pkg1;
	}
	public void setPkg1(int pkg1) {
		this.pkg1 = pkg1;
	}

	public int getPkg4() {
		return pkg4;
	}
	public void setPkg4(int pkg4) {
		this.pkg4 = pkg4;
	}
	
}
